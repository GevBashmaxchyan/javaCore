package lesson9.calc;

public class Calculator {
    int plus(int a, int b) {
        return a + b;
    }

    int minus(int a, int b) {
        return a - b;
    }

    double divide(double a, double b) {
        return a / b;
    }

    long multiple(long a, long b) {
        return a * b;
    }

}
