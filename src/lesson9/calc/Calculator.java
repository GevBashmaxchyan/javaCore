package lesson9.calc;

public class Calculator {
    public int plus(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public double divide(double a, double b) {
        return a / b;
    }

    public long multiple(long a, long b) {
        return a * b;
    }

}
